def fixHeader(root:etree.ElementTree, newestVersion:str) -> tuple[bool, str]:
    """Standardizes format of separator used for version number in schemaLocation attribute and
    ensures this number matches the one found in the Version tag in the record. Also adds the
    metadata rights attribute if not already present"""
    changeMade = False
    changeMessage = ""
    #print(root.tag)
    attributes = root.attrib
    #print(attributes.keys())
    # if no schemaLocation was found
    if all(not re.search("\\{.*\\}schemaLocation", key) for key in attributes.keys()):
        # update Version elt
        updateVersion(root, newestVersion)
    else:
        # key looks like this: {http://www.w3.org/2001/XMLSchema-instance}schemaLocation
        for key, val in attributes.items():
            # fix schemaLocation value
            if "schemaLocation" in key:
                schemaLocation = val
                #print(schemaLocation)
                # replace http with https
                if "http:" in schemaLocation:
                    changeMade = True
                    schemaLocation = schemaLocation.replace("http:", "https:")
                    if "Changed http" in changeMessage:
                        *_, after = changeMessage.partition("top-level")
                        attribChanged, _, _ = after.partition("attribute")
                        changeMessage = changeMessage.replace(attribChanged, f"{attribChanged}/schemaLocation")
                    else:
                        changeMessage = "Changed http to https in top-level schemaLocation attribute."
                # replace underscores or hyphens used as version number separators with periods
                before, sep, versionNumber = schemaLocation.rpartition("/")
                spaseTag, sep, versionNumber = versionNumber.partition("-")
                if "-" in versionNumber or "_" in versionNumber:
                    if "-" in versionNumber:
                        newVersionNum = versionNumber.replace("-", ".")
                    else:
                        newVersionNum = versionNumber.replace("_", ".")       
                    schemaLocation = schemaLocation.replace(versionNumber, newVersionNum)
                    changeMade = True
                    if changeMessage:
                        changeMessage += " Fixed version number separator in top-level schemaLocation attribute."
                    else:
                        changeMessage = "Fixed version number separator in top-level schemaLocation attribute."
                *_, versionNumber = schemaLocation.replace(".xsd", "").rpartition("/")
                *_, versionNumber = versionNumber.partition("spase-")
                if versionNumber != newestVersion:
                    changeMade = True
                    schemaLocation = schemaLocation.replace(versionNumber, newestVersion)
                    """if changeMessage:
                        changeMessage += " Matched version number in schemaLocation attribute to updated value in Version tag."
                    else:
                        changeMessage = "Matched version number in schemaLocation attribute to updated value in Version tag."""
                # verify that the versionNum here matches the one in Version tag, if not update it
                updateVersion(root, newestVersion)
                attributes[key] = schemaLocation
                #print(attributes[key])
    return changeMade, changeMessage

