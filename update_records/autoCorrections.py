def fixHTML_CharCodes(root:etree.ElementTree) -> tuple[bool, etree.ElementTree]:
    # if '&amp;#181;' or '&amp;apos;' like string found in SPASE record, replace '&amp; with just '&'
    rootStr = etree.tostring(root).decode()
    changeMade = False
    #print(rootStr)
    if '&amp;' in rootStr:
        #print(rootStr.replace("&amp;", "&"))
        changeMade = True
        root = etree.fromstring(rootStr.replace("&amp;", "&"))
        #print(root)
    return changeMade, root

