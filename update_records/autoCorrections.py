def getPaths(entry, paths) -> list:
    """Takes the absolute path of a SPASE record directory to be walked
    to extract all SPASE records present. Returns these paths using the
    list parameter paths, which holds the absolute paths generated by
    the function.

    :param entry: A string of the absolute path of the SPASE record directory
                    to be searched/walked to find all SPASE records within.
    :type entry: String
    :param paths: A list to hold absolute paths of all SPASE records found
                    within the given directory
    :type paths: list
    :return: A list containing the absolute paths of all SPASE records found
                within the given directory.
    :rtype: list
    """
    import os
    if os.path.exists(entry):
        for root, dirs, files in os.walk(entry):
            if files:
                for file in files:
                    root = root.replace("\\", "/")
                    paths.append(root + "/" + file)
    else:
        print(entry + " does not exist")
    return paths

def fixFunding(desiredRoot: etree.ElementTree) -> tuple:
    changeMade = False
    changeWhere = []
    # iterate thru to correct funding agency and award texts
    for child in desiredRoot.iter(tag=etree.Element):
        if child.tag.endswith("Funding"):
            targetChild = child
            for child in targetChild:
                if child.tag.endswith("Agency"):
                    if ";" in child.text:
                        agency, sep, name = child.text.partition(";")
                        child.text = agency
                        changeMade = True
                        changeWhere.append("agency")
                    elif ", Mars" in child.text:
                        agency, sep, name = child.text.partition(", ")
                        child.text = agency
                        changeMade = True
                        changeWhere.append("agency")
                elif child.tag.endswith("AwardNumber"):
                    if "contracts" in child.text:
                        child.text = child.text.replace("contracts ", "")
                        changeMade = True
                        changeWhere.append("awardNumber")
    if ("agency" in changeWhere) and ("awardNumber" in changeWhere):
        changeMessage = "Modified Funding Agency and AwardNumber."
    elif "agency" in changeWhere:
        changeMessage = "Modified Funding Agency."
    else:
        changeMessage = "Modified Funding AwardNumber."
    return changeMade, changeMessage

