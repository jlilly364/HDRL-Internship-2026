            if '; ' in newPI:
                newPI = newPI.split("; ")
            if isinstance(newPI, str):
                newPI = [newPI]
            #print(newPI)
            # process each PI name
            for PI in newPI:
                #print(PI)
                PersonID = []
                # if different role is in list, add proper role
                if "(Co-PI)" in PI:
                    role = "CoPI"
                    PI = PI.replace(" (Co-PI)", "")
                elif "(PS)" in PI:
                    role = "ProjectScientist"
                    PI = PI.replace(" (PS)", "")
                elif "(DataProducer)" in PI:
                    role = "DataProducer"
                    PI = PI.replace(" (DataProducer)", "")
                elif "(FormerPI)" in PI:
                    role = "FormerPI"
                    PI = PI.replace(" (FormerPI)", "")
                # format Contact name correctly
                fullName = PI.split(" ")
