def get_parameter_and_quantities(metadata: etree.ElementTree) -> list[dict]:
    # Each object is:
    #   {"name": Name, "alternateName": ParameterKey, "Quantity": "xQuantity:xQuantityVal"}
    variable_measured = []
    param_quantity = ""
    param_name = ""
    key = ""

    root = metadata.getroot()
    # iterate thru xml to get desired info
    for elt in root.iter(tag=etree.Element):
        if (
            elt.tag.endswith("NumericalData")
            or elt.tag.endswith("DisplayData")
            or elt.tag.endswith("DisplayOutput")
            or elt.tag.endswith("NumericalOutput")
            or elt.tag.endswith("Catalog")
        ):
            desiredRoot = elt

    # traverse xml to extract needed info
    for child in desiredRoot.iter(tag=etree.Element):
        if child.tag.endswith("Parameter"):
            qualifier = []
            target_child = child
            for child in target_child:
                try:
                    if child.tag.endswith("Name"):
                        param_name = child.text
                    elif child.tag.endswith("ParameterKey"):
                        key = child.text
                    elif child.tag.endswith("Field"):
                        target_child = child
                        for child in target_child:
                            if child.tag.endswith("FieldQuantity"):
                                param_quantity = f"FieldQuantity:{child.text}"
                            elif child.tag.endswith("Qualifier"):
                                qualifier.append(child.text)
                    elif child.tag.endswith("Particle"):
                        target_child = child
                        for child in target_child:
                            if child.tag.endswith("ParticleQuantity"):
                                param_quantity = f"ParticleQuantity:{child.text}"
                            elif child.tag.endswith("Qualifier"):
                                qualifier.append(child.text)
                    elif child.tag.endswith("Wave"):
                        target_child = child
                        for child in target_child:
                            if child.tag.endswith("WaveQuantity"):
                                param_quantity = f"WaveQuantity:{child.text}"
                            elif child.tag.endswith("Qualifier"):
                                qualifier.append(child.text)
                    elif child.tag.endswith("Mixed"):
                        target_child = child
                        for child in target_child:
                            if child.tag.endswith("MixedQuantity"):
                                param_quantity = f"MixedQuantity:{child.text}"
                            elif child.tag.endswith("Qualifier"):
                                qualifier.append(child.text)
                    elif child.tag.endswith("Support"):
                        target_child = child
                        for child in target_child:
                            if child.tag.endswith("SupportQuantity"):
                                param_quantity = f"SupportQuantity:{child.text}"
                            elif child.tag.endswith("Qualifier"):
                                qualifier.append(child.text)
                except AttributeError:
                    continue
            # most basic entry for variable measured
            entry = {"name": param_name, "quantity": param_quantity}
            """if key:
                entry["alternateName"] = key"""
            if qualifier:
                entry["qualifier"] = qualifier
            variable_measured.append(entry)
    if len(variable_measured) == 0:
        variable_measured = None
    return variable_measured


