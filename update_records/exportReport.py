def findMatchInAllXML(record: str, response: str) -> tuple[bool, str]:
    """Scrapes given NASA SPASE record for productKey which matches a "serviceprovider_ID" found
    in the attributes for datasets in all.xml. When a match is found, update the current
    RelativeStopDate to the one reflected in the "timerange_stop" attribute in all.xml,
    according to 'fuzzy' logic, or replace with a StopDate if related mission has ended.
    """
    # P1D, P1M, and P1Y are examples
    # takes SPASE_Path as arg

    import math
