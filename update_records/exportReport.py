def get_observatoryGrps_and_urls(
    metadata: etree.ElementTree, path: str
) -> Union[dict, None]:
    """
    Uses the get_instrument function to attempt to retrieve all relevant information
    associated with any ObservatoryID (and ObservatoryGroupID) fields
    found in their related SPASE records in order to be used in the prov-o
    wasGeneratedBy property.

    :param metadata: The SPASE metadata object as an XML tree.
    :param path: The absolute file path of the XML file the user wishes to pull info from.

    :returns:   The name, url, and ResourceID for each observatory related to this dataset,
                formatted as a list of dictionaries.
    """
    observatory = {}
    observatoryGroups = []
    observatory_group_ids = []
    if os.path.isfile(path):
        root = metadata.getroot()
        for elt in root.iter(tag=etree.Element):
            if elt.tag.endswith("Observatory"):
                desired_root = elt
        for child in desired_root.iter(tag=etree.Element):
            if child.tag.endswith("ObservatoryGroupID"):
                observatory_group_ids.append(child.text)
        infoURL = get_information_url(metadata)
        # finally, follow that link to grab name and url from there
        if observatory_group_ids:
            for observatory_group_id in observatory_group_ids:
                record = (
                    str(Path.home()).replace("\\", "/")
                    + "/"
                    + observatory_group_id.replace("spase://", "")
                    + ".xml"
                )
                record = record.replace("'", "")
                # print(f"ObsGrpID record path is {record}")
                if os.path.isfile(record):
                    test_spase = SPASE(record)
                    group_name = test_spase.get_name()
                    group_url = test_spase.get_url()
                    group_infoURL = get_information_url(test_spase.metadata)
                    grpStart, grpStop = get_ins_or_obs_Start_and_Stop(
                        test_spase.metadata
                    )
                    if group_url:
                        entry = {
                            "Group_name": group_name,
                            "Group_identifier": {
                                "Group@id": group_url,
                                "Group_value": observatory_group_id,
                            },
                        }
                        if group_infoURL:
                            entry["Group_url"] = group_infoURL
                        if grpStart:
                            entry["Group_startDate"] = grpStart
                        if grpStop:
                            entry["Group_stopDate"] = grpStop
                        observatoryGroups.append(entry)
        if infoURL and observatoryGroups:
            observatory = {"url": infoURL, "ObservatoryGroups": observatoryGroups}
        elif infoURL:
            observatory = {"url": infoURL}
        elif observatoryGroups:
            observatory = {"ObservatoryGroups": observatoryGroups}
        else:
            observatory = None
    return observatory


