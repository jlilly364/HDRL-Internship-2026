    def findMatch(records: list, matchKey: str) -> Union[str, None]:
        "Finds SPASE record with matching ProductKey value"
        matchFound = False
        for record in records:
            parser = etree.XMLParser(remove_blank_text=True)
            if os.path.isfile(record):
                NASAtree = etree.parse(record, parser)
                NASAroot = NASAtree.getroot()

                # find element in tree to iterate over
                for elt in NASAroot.iter(tag=etree.Element):
                    if elt.tag.endswith("NumericalData") or elt.tag.endswith(
                        "DisplayData"
                    ):
                        desiredRoot = elt

                # get ProductKey to see if match
                # iterate thru children to locate Access Information
                for child in desiredRoot.iter(tag=etree.Element):
                    if child.tag.endswith("AccessInformation"):
                        targetChild = child
                        # iterate thru children to locate AccessURL and Format
                        for child in targetChild:
                            if child.tag.endswith("AccessURL"):
                                targetChild = child
                                # iterate thru children to locate URL
                                for child in targetChild:
                                    # check if URL has a product key
                                    if child.tag.endswith("ProductKey"):
                                        prodKey = child.text
                                        if matchKey.strip() == prodKey.strip():
                                            matchFound = True
                                            return record
        if not matchFound:
            return None

